#define MOD 1000000007
typedef Uint32Mod<MOD> Int;
typedef pair<int, int> Bird;
Int fact[2555], invfact[2555], two[2555];
vector<Bird> birds;
vector<int> adj[2555];

Int comb(int n, int k) {
  return fact[n] * invfact[k] * invfact[n - k];
}

int GooseInZooDivOne::count(vector <string> field, int dist) {
  fact[0] = invfact[0] = two[0] = 1;
  for (int i = 1; i <= 2550; i++) {
    fact[i] = fact[i - 1] * i;
    invfact[i] = fact[i].inv();
    two[i] = two[i - 1] + two[i - 1];
  }
  birds.clear();
  int rows = field.size(), cols = field[0].size();
  for (int r = 0; r < rows; r++)
    for (int c = 0; c < cols; c++)
      if (field[r][c] == 'v')
        birds.push_back(Bird(r, c));
  int size = birds.size();
  for (int i = 0; i < size; i++) {
    adj[i].clear();
    for (int j = 0; j < size; j++)
      if (abs(birds[i].first - birds[j].first) + abs(birds[i].second - birds[j].second) <= dist)
        adj[i].push_back(j);
  }
  static bool vis[2555];
  memset(vis, false, sizeof (vis));
  queue<int> q;
  int odd = 0, even = 0, cnt = 0;
  for (int i = 0; i < size; i++) {
    if (vis[i]) continue;
    q.push(i);
    vis[i] = true;
    int temp = 0;
    for (int at; !q.empty(); q.pop()) {
      temp++;
      at = q.front();
      for (int i = 0; i < (int) adj[at].size(); i++)
        if (!vis[adj[at][i]]) {
          vis[adj[at][i]] = true;
          q.push(adj[at][i]);
        }
    }
    cnt += temp;
    temp %= 2;
    if (temp == 0) even++;
    else odd++;
  }
  //pln(odd, even);
  Int res = 0;
  for (int i = 2; i <= odd; i += 2)
    res += two[even] * comb(odd, i);
  if (even > 0)
    res += two[even] - 1;
  return res.val;
}

