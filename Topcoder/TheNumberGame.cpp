const string FIRST = "Manao wins", SECOND = "Manao loses";

string TheNumberGame::determineOutcome(int A, int B) {
  stringstream ss;
  ss << A;
  string X, Y, YY;
  ss >> X;
  ss.clear();
  ss << B;
  ss >> Y;
  YY = Y;
  reverse(YY.begin(), YY.end());
  int len = X.size();
  for (int i = 0; i < len; i++) {
    for (int j = 1; i + j <= len; j++) {
      if (X.substr(i, j) == Y || X.substr(i, j) == YY)
        return FIRST;
    }
  }
  return SECOND;
}

